        self.spec_augmenter = SpecAugmentation(time_drop_width=64,
                                               time_stripes_num=2,
                                               freq_drop_width=8,
                                               freq_stripes_num=2)
        self.bn0 = nn.BatchNorm2d(mel_bins)
        width_mult = 1.
        block = InvertedResidualV3
        input_channel = 32
